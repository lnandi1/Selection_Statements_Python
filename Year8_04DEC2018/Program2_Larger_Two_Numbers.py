# The larger of two numbers
Number_1 = int(input("Enter the 1st Number "))
Number_2 = int(input("Enter the 2nd Number "))
if Number_1 > Number_2:
    print("\n",Number_1, " is the larger of the two numbers ")
elif Number_1 < Number_2:
    print("\n",Number_2, " is the larger of the two numbers ")
else:
    print("\nBoth numbers are equal to each other! ")
